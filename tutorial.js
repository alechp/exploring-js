//////////////////////////////////////////////////////////////////////////////////
//Effects of creating a function
//////////////////////////////////////////////////////////////////////////////////

console.log(`${chalk.yellow("//////////////////////////////////////////")}`);
function Person(name) {
  this.name = name;
}

console.log(
  `\n typeof Person.prototype \n ${chalk.green(typeof Person.prototype)}`
);
console.log(`${chalk.grey("//////////////////////////////////////////")}`);

//////////////////////////////////////////////////////////////////////////////////
//Invoking a function as a constructor
//////////////////////////////////////////////////////////////////////////////////

const alex = new Person("Alex");
console.log(`\n typeof alex \n ${chalk.green(typeof alex)}`);
alex.name;
console.log(`${chalk.yellow("//////////////////////////////////////////")}`);

//////////////////////////////////////////////////////////////////////////////////
//Demonstration of prototypical inheritance
//////////////////////////////////////////////////////////////////////////////////

Person.prototype.greet = function() {
  console.log(`\n Hello, \n ${chalk.green(this.name)}`);
};

console.log(
  `\n alex.hasOwnProperty \n ${chalk.green(alex.hasOwnProperty("greet"))}`
);

console.log(`\n ${chalk.grey("//////////////////////////////////////////")}`);
alex.greet();

console.log(`\n ${chalk.yellow("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.grey("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.yellow("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.grey("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.yellow("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.grey("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.yellow("//////////////////////////////////////////")}`);

console.log(`\n ${chalk.grey("//////////////////////////////////////////")}`);

